import numpy as np

view_mat = np.array([
    [-1.000000,     -0.000000,       -0.000000,       -0.000000],
    [0.000000,      1.000000,        0.000000,        -0.000000],
    [-0.000000,     -0.000000,       -1.000000,       -2.000000],
    [0.000000,      0.000000,        0.000000,        1.000000]
    ]
)


clip_mat = np.array([
    [2.000000,      0.000000,        0.000000,        -1.000000],
    [0.000000,      2.000000,        0.000000,        -1.000000],
    [0.000000,      0.000000,        -0.002000,       -1.000200],
    [0.000000,      0.000000,        0.000000,        1.000000]])

vp_mat = np.matmul(clip_mat, view_mat)


positions = [
    [0.00, 0.00],
    [0.05, 0.00],
    [0.10, 0.00],
    [0.15, 0.00],
    [0.20, 0.00],
    [0.25, 0.00],
    [0.30, 0.00],
    [0.35, 0.00],
    [0.40, 0.00],
    [0.45, 0.00]
]

for p in positions:
    p4 = np.array([p[0], p[1], 0.0, 1.0])

    t = np.matmul(vp_mat, p4)

    print('{} =>\t{}'.format(p, t))